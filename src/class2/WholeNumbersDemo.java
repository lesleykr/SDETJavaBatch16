package class2;

public class WholeNumbersDemo {

    public static void main(String[] args) {

        byte age = 50;
        System.out.println(age);
        short noOfApples = 300;
        System.out.println(noOfApples);

        long kitty = 1000000000000000L;
        System.out.println(kitty);
    }
}
